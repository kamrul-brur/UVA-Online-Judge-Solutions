/*
10469 - To Carry or not to Carry
*/

#include<bits/stdc++.h>
using namespace std;

typedef unsigned long long ll;

int main()
{
    int a,b,ans;
    while((cin>>a))
    {
        cin>>b;
        ans = a^b;
        cout<<ans<<endl;
        ans = 0;
    }
    return 0;
}
/*
Sample Input
4 6
6 9

Sample Output
2
15
*/
